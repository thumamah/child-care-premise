                        <h2>Infant/toddlers</h2>
                        <p>The capacity to communicate is the ability and desire to connect with others
                            by exchanging ideas and feelings, both verbally and non-verbally.
                            Most children learn to communicate to get their needs met or
                            to establish interaction with a loved adult.
                            Babies communicate from birth, through sounds (crying, cooing),
                            facial expressions (eye contact, smiling, grimacing),
                            and gestures/body movements. After age one,
                            children increase their peer interactions.
                            Toddlers begin to make attempts to get peers’ attention.</p>
                        <h3>Strategies</h3>
                        <p>Model words for children to use in conflict situations such as “Mine” or “Stop!”
                            Even very young children can learn to use simple words such as these.
                            Share phrases you are teaching toddlers with their family members</p>
                        <p> Help each child develop vocabulary for how they are feeling. “You’re sad because we could not stay longer and play at the park” or
                            “You feel mad because you wanted to play with the truck next.”</p>
                        <p> Encourage children to work in groups when possible. Pair children up to complete
                            simple classroom tasks such as picking up toy cars, blocks, etc.
                            Ask children to help pass out napkins, etc. together in class.</p>
                        <p> Teach each child about non-verbal communication. “Tim, do you see how Abby has her hands over her ears? She doesn’t like it when you scream.
