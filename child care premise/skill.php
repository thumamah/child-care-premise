                        <h2>Preschool</h2>
                        <p>Social skills are behaviors that promote positive interactions among people.
                            Some examples of important social skills for young children are sharing, taking turns,
                            cooperating and communicating clearly.
                            Not surprisingly, children who have poor social skills often have a hard time getting
                            along with other children. Fortunately,
                            daily opportunities can be created to help strengthen these skills in children.</p>
                        <h3>Strategies</h3>
                        <p>Practicing social skills is a way to work on specific aspects of social interactions. 
                            For example, if you notice a child stands too close to peers or has difficulty getting a classmate’s attention, 
                            help them learn about personal space or conversational skills through role play in the classroom.
                            Create stories to help teach these skills.</p>
                        <p> While reading books, ask children many questions about how the characters may be feeling and why.
                             Ask the children what someone in the story could do to make a character feel better.</p>
                        <p> Pair the children up for certain activities.
                             Assign a child who has difficulty interacting to work with another child with a similar temperament.
